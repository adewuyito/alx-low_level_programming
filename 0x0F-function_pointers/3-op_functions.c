#include "3-calc.h"

/**
 * op_add - Add two numbers
 * @a: first number
 * @b: second number
 *
 * Return: The result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract two numbers
 * @a: first number
 * @b: second number
 *
 * Return: The result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two numbers
 * @a: first number
 * @b: second number
 *
 * Return: The result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two numbers
 * @a: first number
 * @b: second number
 *
 * Return: The result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod on two numbers
 * @a: first number
 * @b: second number
 *
 * Return: The result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
